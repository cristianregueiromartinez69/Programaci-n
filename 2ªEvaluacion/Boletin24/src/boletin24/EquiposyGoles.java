
package boletin24;


public class EquiposyGoles {
    
    private String equipo;
    private int goles;
    
    public EquiposyGoles(){
        
        
        
    }

    public EquiposyGoles(String equipo, int goles) {
        this.equipo = equipo;
        this.goles = goles;
    }

    public String getEquipo() {
        return equipo;
    }

    public void setEquipo(String equipo) {
        this.equipo = equipo;
    }

    public int getGoles() {
        return goles;
    }

    public void setGoles(int goles) {
        this.goles = goles;
    }
    
    
    
    
    
    
    
}
