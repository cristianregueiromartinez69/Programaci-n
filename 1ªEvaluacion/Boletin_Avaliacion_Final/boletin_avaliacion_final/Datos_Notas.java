
package boletin_avaliacion_final;


public class Datos_Notas {
    
    
    private float prueba_escrita;
private float prueba_practica;
private float boletins;
private float nota_media;

public Datos_Notas() {


}



public Datos_Notas(float prueba_escrita, float prueba_practica, float boletins, float nota_media) {

	this.prueba_escrita = prueba_escrita;
	this.prueba_practica = prueba_practica;
	this.boletins = boletins;
	this.nota_media = nota_media;
}



public float getPrueba_escrita() {
	return prueba_escrita;
}



public void setPrueba_escrita(float prueba_escrita) {
	this.prueba_escrita = prueba_escrita;
}



public float getPrueba_practica() {
	return prueba_practica;
}



public void setPrueba_practica(float prueba_practica) {
	this.prueba_practica = prueba_practica;
}



public float getBoletins() {
	return boletins;
}



public void setBoletins(float boletins) {
	this.boletins = boletins;
}



public float getNota_media() {
	return nota_media;
}



public void setNota_media(float nota_media) {
	this.nota_media = nota_media;
}
























    
    
    
    
    
    
    
    
}
